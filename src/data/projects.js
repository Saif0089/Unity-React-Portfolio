// ── UTILITY FUNCTIONS ────────────────────────────────────────────────

export const getFeaturedProjects = () => PROJECTS.filter((p) => p.featured);

export const getProjectsByCategory = (categoryId) =>
  PROJECTS.filter((p) => p.category === categoryId);

export const getProjectBySlug = (slug) => PROJECTS.find((p) => p.slug === slug);

export const getRelatedProjects = (project, limit = 3) =>
  PROJECTS.filter((p) => p.id !== project.id && p.category === project.category).slice(0, limit);

export const getAllTechnologies = () => {
  const techs = new Set();
  PROJECTS.forEach((p) => p.technologies.forEach((t) => techs.add(t)));
  return [...techs].sort();
};

export const getAllPlatforms = () => {
  const plats = new Set();
  PROJECTS.forEach((p) => p.platforms.forEach((t) => plats.add(t)));
  return [...plats].sort();
};
